def clean_dataset(df, columns_to_check=None, fill_strategy='mean'):
    """
    Clean a pandas DataFrame by removing duplicates and handling missing values.
    
    Parameters:
    df (pd.DataFrame): Input DataFrame to clean.
    columns_to_check (list, optional): List of columns to check for duplicates.
                                       If None, checks all columns.
    fill_strategy (str): Strategy for filling missing values.
                         Options: 'mean', 'median', 'mode', or 'drop'.
    
    Returns:
    pd.DataFrame: Cleaned DataFrame.
    """
    original_shape = df.shape
    
    # Remove duplicates
    if columns_to_check is None:
        df_clean = df.drop_duplicates()
    else:
        df_clean = df.drop_duplicates(subset=columns_to_check)
    
    duplicates_removed = original_shape[0] - df_clean.shape[0]
    
    # Handle missing values
    missing_before = df_clean.isnull().sum().sum()
    
    if fill_strategy == 'drop':
        df_clean = df_clean.dropna()
    elif fill_strategy in ['mean', 'median']:
        numeric_cols = df_clean.select_dtypes(include=[np.number]).columns
        for col in numeric_cols:
            if fill_strategy == 'mean':
                fill_value = df_clean[col].mean()
            else:  # median
                fill_value = df_clean[col].median()
            df_clean[col] = df_clean[col].fillna(fill_value)
    elif fill_strategy == 'mode':
        for col in df_clean.columns:
            if df_clean[col].dtype == 'object':
                mode_value = df_clean[col].mode()
                if not mode_value.empty:
                    df_clean[col] = df_clean[col].fillna(mode_value.iloc[0])
    
    missing_after = df_clean.isnull().sum().sum()
    
    # Print cleaning summary
    print(f"Original dataset shape: {original_shape}")
    print(f"Duplicates removed: {duplicates_removed}")
    print(f"Missing values before cleaning: {missing_before}")
    print(f"Missing values after cleaning: {missing_after}")
    print(f"Cleaned dataset shape: {df_clean.shape}")
    
    return df_clean

def validate_dataframe(df, required_columns=None, min_rows=1):
    """
    Validate DataFrame structure and content.
    
    Parameters:
    df (pd.DataFrame): DataFrame to validate.
    required_columns (list): List of required column names.
    min_rows (int): Minimum number of rows required.
    
    Returns:
    bool: True if validation passes, False otherwise.
    """
    if not isinstance(df, pd.DataFrame):
        print("Error: Input is not a pandas DataFrame")
        return False
    
    if df.empty:
        print("Error: DataFrame is empty")
        return False
    
    if len(df) < min_rows:
        print(f"Error: DataFrame has fewer than {min_rows} rows")
        return False
    
    if required_columns:
        missing_cols = [col for col in required_columns if col not in df.columns]
        if missing_cols:
            print(f"Error: Missing required columns: {missing_cols}")
            return False
    
    return True

