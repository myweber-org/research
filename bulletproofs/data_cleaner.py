def clean_data_with_key(data, key_func=None):
    """
    Remove duplicates based on a key function.
    If key_func is None, uses the item itself.
    """
    seen = set()
    result = []
    for item in data:
        key = key_func(item) if key_func else item
        if key not in seen:
            seen.add(key)
            result.append(item)
